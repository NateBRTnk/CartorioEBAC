int registro() //Função responsavel por cadastras os usuários no sistema
{
		//inicio criação de variáveis/string	
		char arquivo [40];
		char cpf[40];
		char nome[40];
		char sobrenome[40];
		char cargo[40];
		//final criação de variáveis/string
		
		printf("Digite o CPF a ser cadastrado: "); //coletando informação do usuario
		scanf("%s", cpf); //%s refere-se a string
		
		strcpy(arquivo, cpf); //Responsavel por copiar os valores das strings
		
		FILE *file; // cria o arquivo
		file = fopen(arquivo, "w"); // cria o arquivo
		fprintf(file,cpf); // salva o valor da variavel
		fclose(file); // fecha o arquivo
		
		file = fopen(arquivo, "a");
		fprintf(file, ",");
		fclose(file);
		
		printf("Digite o nome a ser cadastrado: ");
		scanf("%s" ,nome);
		
		file = fopen(arquivo, "a");
		fprintf(file,nome);
		fclose(file);
		
		file = fopen(arquivo, "a");
		fprintf(file, ",");
		fclose(file);
		
		
		printf("Digite o sobrenome a ser cadastro: ");
		scanf("%s",sobrenome);
		
		file = fopen(arquivo, "a");
		fprintf(file,sobrenome);
		fclose(file);
		
		file = fopen(arquivo, "a");
		fprintf(file, ",");
		fclose(file);
		
		printf("Digite o cargo a ser cadastrado: ");
		scanf("%s",cargo);
		
		file = fopen(arquivo, "a");
		fprintf(file,cargo);
		fclose(file);
		
		system("pause");
}

int consulta() //Função responsavel por consultar os usuários no sistema
{
	
	setlocale(LC_ALL, "Portuguese"); //Linguagem definida
	//inicio criação de variáveis/string
	char cpf[40];
	char conteudo[200];
	//final criação de variáveis/string
	
	printf("Digite o CPF a ser consultado");
	scanf("%s",cpf);
	
	FILE *file;
	file = fopen(cpf,"r");
	
	if(file == NULL)
	{
		printf("Não foi possivel abrir o arquivo, nao localizado!.\n");
	}
	
	while(fgets(conteudo, 200, file) != NULL)
	{
		printf("\nEssas são as informações do usario: ");
		printf("%s", conteudo);
		printf("\n\n");
		
	}
	
	system("pause");
}

